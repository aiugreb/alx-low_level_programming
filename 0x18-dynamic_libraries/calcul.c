#include <stdio.h>
/**
 * add - add two integers
 * @a: first int
 * @b: second int
 * Return: addition
*/
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtract two integers
 * @a: first int
 * @b: second int
 * Return: subtraction
*/
int sub(int a, int b)
{
        return (a - b);
}
/**
 * div - divid two integers
 * @a: first int
 * @b: second int
 * Return: result
*/
int div(int a, int b)
{
        return (a / b);
}
/**
 * mod - modulo of two integers
 * @a: first int
 * @b: second int
 * Return: answer
*/
int mod(int a, int b)
{
        return (a % b);
}
/**
 * mul - multiply two integers
 * @a: first int
 * @b: second int
 * Return: result
*/
int mul(int a, int b)
{
        return (a * b);
}
